package com.example.floatingtest;

public class sourceConceptItem {
	private String id;
	private String name;
	private String url;
	
	public String getID()
	{
		return id;
	}
	
	public String getName()
	{
		return name;
	}
	
	public String getURL()
	{
		return url;
	}
	
	public String toString()
	{
		return id + " " + name + " " + url;
	}

}
